public class loops {
    public static void main(String[] args) {
        int i = 1;
        while (i <=9){
            System.out.println(i);
            i++;
        }
        System.out.println("Done.");

        for (int j = 0; j <= 9; j++) {
            System.out.println(j);
        }
        System.out.println("Done1.");
    }
}
